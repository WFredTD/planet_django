from django.db import models

# Classe: Uma classe é um modelo ou um template que define as
# propriedades (atributos) e comportamentos (métodos) que os objetos criados a partir dela terão.
# Classe é um agrupamento de objetos com características e comportamentos semelhantes.
# Pense em uma classe como um molde para fazer objetos. É como a receita de um bolo.

# Objeto: Um objeto é uma instância de uma classe.
# Ele representa uma entidade concreta que possui estado e
# cada instância pode ter valores diferentes para seus atributos, mas compartilha
# a mesma estrutura e comportamento definidos pela classe.
# Um objeto é o bolo feito a partir do molde (classe). É algo concreto que você pode ver e usar.
# Se você fizer vários bolos com a mesma receita, cada bolo é uma instância.

# Construtor: Um construtor é um método especial de uma classe que é
# chamado automaticamente quando um objeto é criado. Ele é usado para inicializar os atributos do objeto.
# O construtor é como o processo de misturar os ingredientes e assar o bolo. Ele prepara o objeto quando você o cria.

# Método: Um método é uma função definida dentro de uma classe que descreve
# um comportamento que os objetos daquela classe podem executar.
# Um método é uma ação que o objeto pode realizar, como cortar o bolo ou servir uma fatia.

# Atributo: Um atributo é uma variável que pertence a uma classe e
# representa o estado ou as características dos objetos daquela classe.
# Um atributo é uma característica do objeto, como o sabor do bolo ou a quantidade de fatias.


